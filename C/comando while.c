#include <stdio.h>
#include <stdlib.h>

int main () {
	int i=1;
	// WHILE = ENQUANTO
	// CODIGO SIMPLES DE CONTAGEM DE 1 ATE 10
	// (I <=10) = ENQUANTO I FOR MENOR IGUAL A 10 
	// { = FAÇA
	// } = FIMENQUANTO
	// I++ = O DADO I QUE COMEÇA VALENDO 1 SEMPRE QUE O LAÇO PASSA POR ESSE CODIGO O VALOR DE I GANHA MAIS UMA UNIDADE.
	while(i <=10){ 
		printf("%i ", i);
		i++;
		
	}
}
